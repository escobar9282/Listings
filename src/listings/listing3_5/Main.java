package listings.listing3_5;

public class Main
{
    public static void main(String[] args)
    {
        int i = 1;

        while (i <= 100)
        {
            if (i < 100)
            {
                System.out.print(i + ", ");
            }
            else
            {
                System.out.println(i);
            }
            i++;
        }
    }
}
